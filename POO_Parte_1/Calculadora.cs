using System;
namespace Modelo_Console_POO
{
    public static class Calculadora
    {
        
        //Usando classes e métodos estáticos

        public static double Multiplica(double NumA, double NumB)
        {
            return NumA * NumB;
        }

        public static double Soma(double NumA, double NumB)
        {
            return NumA + NumB;
        }



    }
